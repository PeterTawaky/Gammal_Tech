#include <stdio.h>
#include <stdlib.h>

int main()
{
    int x;
    printf("Please,enter a number: ");
    scanf("%d",&x);
    printf("x= %d",x);         // x
    printf("\nx++= %d",x++);   // x
    printf("\n++x= %d",x);     // x+1
    printf("\nx+1= %d",x+1);   // x+1
    printf("\nx--= %d",x--);   // x
    printf("\n--x= %d",--x);   // x-1
    printf("\nx-1= %d",x-1);   // x-1




    return 0;
}
